    public void setTimestamp(String timestamp) {
        this.timestamp = timestamp;
    }

    public String getTokengoogle() {
        return tokengoogle;
    }

    public void setTokengoogle(String tokengoogle) {
        this.tokengoogle = tokengoogle;
    }

